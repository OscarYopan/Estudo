//OPERADORES DE LÓGICOS

/*
&& => and => e
  - verifica se duas ou mais afirmações são 'true' ou 'false'
  - para retornar 'true' todas devem ser verdadeiras
  - para retornar 'false' basta uma ser falsa
|| => or => ou
  - verifica se uma entre duas ou mais afirmações são 'true' ou 'false'
! => not => não
  - inverte uma expressão
*/
console.log(true && true && true && true);

console.log(true && true && false && true);

console.log(true || true || true);

console.log(false || false || false)

console.log(true || false || true)

console.log(!true);

console.log(!false);

console.log(!!true); // é acumulativo




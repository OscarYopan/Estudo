/*
  Constantes
  - Não se deve usar palavras reservadas (if, else, let ...);
  - Recomenda-se usar nomes significativos;
  - Não pode começar o nome da constante com um numero;
  - Recomenda-se usar letras minusculas no começo;
  - não deve conter 'espaços' ou 'traços';
  - Caso nome composto use camelCase;
  - Constantes são case sensetive (usa-se escrita totalmente igual);
  - Não se pode mudar o valor de uma constante;
  - Sempre que declarada já deve ser definido o valor;
  - valor da constante é imutavel.
  */
